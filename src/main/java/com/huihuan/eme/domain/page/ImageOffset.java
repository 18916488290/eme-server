package com.huihuan.eme.domain.page;

public class ImageOffset {
	
  private int width;
  private int height;
  
  public ImageOffset()
  {
	  this.width=0;
	  this.height=3;
  }
/**
 * @return the width
 */
public int getWidth() {
	return width;
}
/**
 * @param width the width to set
 */
public void setWidth(int width) {
	this.width = width;
}
/**
 * @return the height
 */
public int getHeight() {
	return height;
}
/**
 * @param height the height to set
 */
public void setHeight(int height) {
	this.height = height;
}
	
	

}
